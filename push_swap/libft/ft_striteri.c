#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int	cnt;

	cnt = 0;
	while (s[cnt] != '\0')
	{
		f(cnt, &s[cnt]);
		cnt++;
	}
}
